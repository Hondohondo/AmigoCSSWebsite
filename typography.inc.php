<h3 class="font-size-24 orange-09 margin-bottom-16">Typography</h3>
<p class="margin-bottom-8">
    Typography is one of the most important part of any web creation process. Amigo CSS makes it easy for you to come up with values for designing and formatting text.
</p>
<p class="margin-bottom-8">
Amigo CSS Typography is adapted from a minor third (1.2) type scale with a base font of 10px.
</p>
<p class="margin-bottom-12">
    Built with developers' creative freedom in mind, Amigo CSS doesn't limit you. Be free to add your own values if these don't suit your needs. But again, for most a system like this should suffice.
</p>

