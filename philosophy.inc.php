<h3 class="font-size-24 orange-09 margin-bottom-16">Philosophy</h3>
<p class="margin-bottom-8">
    If there is one thing special about Amigo CSS is its philosophy. Amigo CSS philosophy is based on primarily two things:
</p>
<p class="">
  <strong>1.</strong>  Using a custom class (and hence custom-first). It can be any class name you prefer
</p>
<p>
  <strong>2.</strong>  Using a maximum of seven utility classes per element (You can use less, but a maximum is seven)
</p>

<p class="max-width-240 padding-16 margin-center margin-bottom-8 background-white border-radious-5">
    <code>
        1 Custom Class
        <br>
        7 Utility Classes
    </code>

</p>
<p class="margin-bottom-8">
    Before diving deep into the philosophy, let's see exactly what we mean when we say Amigo CSS is a <span class="font-weight-600">simple</span>, <span class="font-weight-600">custom-first </span>and <span class="font-weight-600">intuitive</span> framework for
    <span class="font-weight-600">CSS lovers</span>
</p>
<br>
<div class="philosophy margin-left-12">
    <p class="display-flex">
        <span class="padding-right-8">&bull;</span>
        <span>
            <strong>Simple</strong>
        &mdash; Amigo CSS is simple in the way that it's super easy to get started just like you will see soon. It uses plain
        vanilla CSS and it has absolutely no single line of JavaScript code.</span>

    </p>
    <br>
    <p class="display-flex">
        <span class="padding-right-8">&bull;</span>
        <span>
            <strong>Custom-first</strong>
        &mdash; Based on its philosophy, Amigo CSS is "custom-first". I believe I may have coined this term in response to
        utility-first. Amigo CSS uses utility classes but according to its philosophy it's not utility-first, it's
        custom-first. What's custom first? Custom-first is a custom css class made by a developer. In Amigo CSS a first
        class in any element 'has' to be a custom class. It's actually not a must...but it's recommended if you want to
        adhere to the philosophy. Do you need to have a custom class? No! You don't ...Again it's a philosophy.</span>

    </p>
    <br>
    <p class="display-flex">
        <span class="padding-right-8">&bull;</span>
        <span>
            <strong>Intuitive</strong>
        &mdash; Amigo CSS doesn't use any guess work in naming its classes. Can you take a guess what these classes do:
        <em>.display-flex</em>,
        <em>.display-grid</em>,
        <em>.font-size-16</em>, How about
        <em>.margin-center</em>?
        You get the point.</span>

    </p>
    <br>

    <p class="display-flex">
        <span class="padding-right-8">&bull;</span>
        <span>
            <strong>CSS Lovers</strong>
        &mdash;   Amigo CSS is for those people who love CSS and love to write CSS code. As you will come to see you need to have
        some knowledge...whatever little it may be of CSS to be able to enjoy Amigo CSS. This framework is for all the
        CSS lovers out there!</span>
    </p>

</div>

