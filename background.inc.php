<h3 class="font-size-24 orange-09 margin-bottom-16">Background</h3>
<p class="margin-bottom-8">
    In this first release of Amigo CSS, we will deal with only Background colors, we will add more background properties later. In Amigo CSS, background colors follow the same system as our color system. So you have the same number of options of colors to pick from. Just like the color system it's also adapted from Open colors and comes with 6 variations instead of 10 variations per color.
</p>
<p class="margin-bottom-8">
Different from the color system, I have decided to display these background colors in a vertical order to make it maybe even easier to differentiate between different tints (lightness) and shades (darkness) of colors
</p>
<p class="margin-bottom-12">
&nbsp;
</p>

